package fundamentos.operadores;

public class Relacionais {
	
	public static void main(String[] args) {
		
		int a = 97;
		int b = 'a';
		
		// == Diferente
		System.out.println(a == b);
		
		// > Maior que
		System.out.println( 3 > 4);
		
		// < Menor que
		System.out.println( 3 < 4);
		
		// >= maior ou igual a
		System.out.println( 3 >= 4);
		
		// <= menor ou igual a
		System.out.println( 3 <= 4);
		
		// != diferente de 
		System.out.println( 3 != 4);
		
		
		
		
		
	}

}
